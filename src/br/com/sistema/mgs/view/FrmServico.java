    private void table_servicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_servicosMouseClicked
        jTabbedPane1.setSelectedIndex(0);
        codServico.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 0).toString());
        codCliente.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 1).toString());
        nome.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 2).toString());
        equipamento.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 3).toString());
        marca.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 4).toString());
        modelo.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 5).toString());
        descricao.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 6).toString());
        servico.setSelectedItem(String.valueOf(table_servicos.getValueAt(table_servicos.getSelectedRow(), 7).toString()));
        valor.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 8).toString());
        data.setText(table_servicos.getValueAt(table_servicos.getSelectedRow(), 9).toString());
    }//GEN-LAST:event_table_servicosMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        listar();
        Date agora = new Date();
        SimpleDateFormat dataBr = new SimpleDateFormat("dd/MM/yyyy");
        String dataFormatada = dataBr.format(agora);
        data.setText(dataFormatada);
    }//GEN-LAST:event_formWindowActivated

