    public void listar() {
        ServicoDAO dao = new ServicoDAO();
        List<Servico> lista = dao.listarServicos();
        DefaultTableModel dados = (DefaultTableModel) table_servicos.getModel();
        dados.setNumRows(0);
        for (Servico s : lista) {
            dados.addRow(new Object[]{
                s.getId(),
                s.getCliente().getId(),
                s.getCliente().getNome(),
                s.getEquipamento(),
                s.getMarca(),
                s.getModelo(),
                s.getDescricao(),
                s.getServico_prestado(),
                s.getValor(),
                s.getData_servico()
            });
        }
    }

