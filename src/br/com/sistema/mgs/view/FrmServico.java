    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        objs = new Servico();

        if (codCliente.getText().isEmpty() || nome.getText().isEmpty() || equipamento.getText().isEmpty() || marca.getText().isEmpty() || modelo.getText().isEmpty()
                || descricao.getText().isEmpty() || valor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos em branco.");
        } else {
            Cliente c = new Cliente();
            c.setId(Integer.parseInt(codCliente.getText()));
            objs.setCliente(c);
            objs.setEquipamento(equipamento.getText());
            objs.setMarca(marca.getText());
            objs.setModelo(modelo.getText());
            objs.setDescricao(descricao.getText());
            objs.setServico_prestado(servico.getSelectedItem().toString());
            objs.setValor(Double.parseDouble(valor.getText().replaceAll(",", ".")));
            objs.setData_servico(data.getText());
            Date agora = new Date();
            SimpleDateFormat dataEUA = new SimpleDateFormat("yyyy-MM-dd");
            String dataFormatada = dataEUA.format(agora);
            objs.setData_servico(dataFormatada);
            daos = new ServicoDAO();
            daos.cadastrarServicos(objs);
            new Utilitarios().LimpaTela(dados_cliente);
            new Utilitarios().LimpaTela(dados_servico);
            descricao.setText("");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        objs = new Servico();
        Cliente c = new Cliente();
        c.setId(Integer.parseInt(codCliente.getText()));
        objs.setCliente(c);
        objs.setId(Integer.parseInt(codServico.getText()));
        objs.setEquipamento(equipamento.getText());
        objs.setMarca(marca.getText());
        objs.setModelo(modelo.getText());
        objs.setDescricao(descricao.getText());
        objs.setServico_prestado(servico.getSelectedItem().toString());
        objs.setValor(Double.parseDouble(valor.getText().replaceAll(",", ".")));
        objs.setData_servico(data.getText());
        daos = new ServicoDAO();
        daos.editarServico(objs);
        new Utilitarios().LimpaTela(dados_cliente);
        new Utilitarios().LimpaTela(dados_servico);
        descricao.setText("");
    }//GEN-LAST:event_btnEditarActionPerformed

