    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Cliente obj = new Cliente();
        if (nome.getText().isEmpty() || logradouro.getText().isEmpty() || bairro.getText().isEmpty()
                || cidade.getText().isEmpty() || cep.getText().isEmpty() || email.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos que contem '*' ");
        } else {
            obj.setNome(nome.getText());
            obj.setLogradouro(logradouro.getText());
            obj.setNumero(Integer.parseInt(numero.getText()));
            obj.setBairro(bairro.getText());
            obj.setCidade(cidade.getText());
            obj.setCep(cep.getText());
            obj.setEstado(estado.getSelectedItem().toString());
            obj.setCnpj(cnpj.getText());
            obj.setCpf(cpf.getText());
            obj.setInscricao_estadual(inscricao_estadual.getText());
            obj.setEmail(email.getText());
            obj.setTelefone(telefone.getText());
            obj.setCelular(celular.getText());

            ClienteDAO dao = new ClienteDAO();
            dao.cadastrarCliente(obj);
            new Utilitarios().LimpaTela(JPDadosPessoais);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

