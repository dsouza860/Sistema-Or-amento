    public void listar() {
        ClienteDAO dao = new ClienteDAO();
        List<Cliente> lista = dao.listaClientes();
        DefaultTableModel dados = (DefaultTableModel) table_clientes.getModel();
        dados.setNumRows(0);

        for (Cliente c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getLogradouro(),
                c.getNumero(),
                c.getBairro(),
                c.getCidade(),
                c.getCep(),
                c.getEstado(),
                c.getCnpj(),
                c.getCpf(),
                c.getInscricao_estadual(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular()
            });
        }
    }

