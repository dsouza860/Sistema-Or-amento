    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        if (nome.getText().isEmpty() || email.getText().isEmpty() || logradouro.getText().isEmpty()
                || bairro.getText().isEmpty() || cidade.getText().isEmpty() || equipamento.getText().isEmpty()
                || modelo.getText().isEmpty() || marca.getText().isEmpty() || descricao.getText().isEmpty()
                || valor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos que contem '*' ");
        } else {
            OrcamentoDAO dao = new OrcamentoDAO();
            List lista_dados = getDados();
            try {
                dao.ImprimirOrcamento(lista_dados);
            } catch (Exception e) {
                e.printStackTrace();
            }

            new Utilitarios().LimpaTela(dadosClliente);
            new Utilitarios().LimpaTela(descricaoServico);
            descricao.setText("");
        }

    }//GEN-LAST:event_btnImprimirActionPerformed

