    private void btnPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisarMouseClicked
        if (txtCodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o código!");
        } else {
            Cliente obj = new Cliente();
            ClienteDAO dao = new ClienteDAO();
            obj = dao.consultaPorCodigo(Integer.parseInt(txtCodigo.getText()));
            nome.setText(obj.getNome());
            logradouro.setText(obj.getLogradouro());
            numero.setText(String.valueOf(obj.getNumero()));
            bairro.setText(obj.getBairro());
            cidade.setText(obj.getCidade());
            cep.setText(obj.getCep());
            estado.setSelectedItem(obj.getEstado());
            cnpj.setText(obj.getCnpj());
            cpf.setText(obj.getCpf());
            inscricao_estadual.setText(obj.getInscricao_estadual());
            email.setText(obj.getEmail());
            telefone.setText(obj.getTelefone());
            celular.setText(obj.getCelular());
        }

    }//GEN-LAST:event_btnPesquisarMouseClicked

