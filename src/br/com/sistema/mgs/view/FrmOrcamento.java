    private void cepKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cepKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Cliente obj = new Cliente();
            ClienteDAO dao = new ClienteDAO();
            obj = dao.buscaCep(cep.getText());
            logradouro.setText(obj.getLogradouro());
            bairro.setText(obj.getBairro());
            cidade.setText(obj.getCidade());
            estado.setSelectedItem(obj.getEstado());

        }
    }//GEN-LAST:event_cepKeyPressed
    public List getDados() {
        List lista = new ArrayList();
        Orcamento print = new Orcamento();
        print.setNome(nome.getText());
        print.setLogradouro(logradouro.getText());
        print.setNumero(Integer.parseInt(numero.getText()));
        print.setBairro(bairro.getText());
        print.setCidade(cidade.getText());
        print.setCep(cep.getText());
        print.setEstado(estado.getSelectedItem().toString());
        print.setCnpj(cnpj.getText());
        print.setCpf(cpf.getText());
        print.setInscricao_estadual(inscricao_estadual.getText());
        print.setEmail(email.getText());
        print.setTelefone(telefone.getText());
        print.setCelular(celular.getText());
        print.setEquipamento(equipamento.getText());
        print.setMarca(marca.getText());
        print.setModelo(modelo.getText());
        print.setDescricao(descricao.getText());
        print.setServico_prestado(servico_prestado.getSelectedItem().toString());
        print.setValor(Double.parseDouble(valor.getText()));
        print.setData_servico(data_servico.getText());
        lista.add(print);
        return lista;
    }

